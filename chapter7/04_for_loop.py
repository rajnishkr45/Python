# Syntax of for loop

# for variabe_name in range_function(starting_value , ending_value + 1, skip_step)
# skip step -> gap between two iteration by default it is 1
# maximum time we dont write starting value as by default it is 0

# shorterst example
for i in range(11):
    print(i)

# it will print 0 to 10


# print 1 to 50 using for loop
for i in range(1, 51):
    print(i)


# Print the table of 4
for i in range(4, 44, 4):
    print(i)
